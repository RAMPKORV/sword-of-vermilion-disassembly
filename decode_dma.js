// Parse all bytes
const allBytes = [];
for (const line of rawLines) {
  const tokens = line.split(',').map(t => parseInt(t.trim().replace('$', ''), 16));
  allBytes.push(...tokens);
}

const ENTRY_SIZE = 16;
const count = allBytes.length / ENTRY_SIZE;
console.log(`Total bytes: ${allBytes.length}, entries: ${count}`);

let errors = 0;
const entries = [];

for (let i = 0; i < count; i++) {
  const o = i * ENTRY_SIZE;
  const b = allBytes.slice(o, o + ENTRY_SIZE);

  // Validate fixed bytes
  if (b[0] !== 0x94) { console.error(`Entry ${i}: b[0]=$${b[0].toString(16)} expected $94`); errors++; }
  if (b[2] !== 0x93) { console.error(`Entry ${i}: b[2]=$${b[2].toString(16)} expected $93`); errors++; }
  if (b[4] !== 0x96) { console.error(`Entry ${i}: b[4]=$${b[4].toString(16)} expected $96`); errors++; }
  if (b[5] !== 0xD0) { console.error(`Entry ${i}: b[5]=$${b[5].toString(16)} expected $D0`); errors++; }
  if (b[6] !== 0x95) { console.error(`Entry ${i}: b[6]=$${b[6].toString(16)} expected $95`); errors++; }
  if (b[7] !== 0x00) { console.error(`Entry ${i}: b[7]=$${b[7].toString(16)} expected $00`); errors++; }
  if (b[8] !== 0x97) { console.error(`Entry ${i}: b[8]=$${b[8].toString(16)} expected $97`); errors++; }
  if (b[9] !== 0x7F) { console.error(`Entry ${i}: b[9]=$${b[9].toString(16)} expected $7F`); errors++; }
  if (b[14] !== 0x00) { console.error(`Entry ${i}: b[14]=$${b[14].toString(16)} expected $00`); errors++; }
  if (b[15] !== 0x00) { console.error(`Entry ${i}: b[15]=$${b[15].toString(16)} expected $00`); errors++; }

  const lenWords = (b[1] << 8) | b[3];

  // Reconstruct the 4-byte DMA destination command (big-endian)
  const cmd = ((b[10] << 24) | (b[11] << 16) | (b[12] << 8) | b[13]) >>> 0;

  // Decode VRAM address from command:
  // cmd = $40000080 | ((vram_addr & $3FFF) << 16) | ((vram_addr >> 14) & 3)
  // Verify the constant bits
  const constBits = cmd & ~(0x3FFF0000 | 0x3);
  if (constBits !== 0x40000080) {
    console.error(`Entry ${i}: cmd=$${cmd.toString(16).toUpperCase().padStart(8,'0')} constant bits=$${constBits.toString(16)} expected $40000080`);
    errors++;
  }

  const vramAddr = ((cmd >> 16) & 0x3FFF) | ((cmd & 3) << 14);

  // Round-trip check
  const cmdCheck = (0x40000080 | ((vramAddr & 0x3FFF) << 16) | ((vramAddr >> 14) & 3)) >>> 0;
  if (cmdCheck !== cmd) {
    console.error(`Entry ${i}: round-trip fail cmd=$${cmd.toString(16)} check=$${cmdCheck.toString(16)}`);
    errors++;
  }

  entries.push({ lenWords, vramAddr, cmd });
}

if (errors > 0) {
  console.error(`\n${errors} validation errors found!`);
  process.exit(1);
}

console.log(`\nAll ${count} entries validated OK.\n`);

// Emit macro calls
for (let i = 0; i < entries.length; i++) {
  const { lenWords, vramAddr } = entries[i];
  const lenHex = '$' + lenWords.toString(16).toUpperCase().padStart(4, '0');
  const vramHex = '$' + vramAddr.toString(16).toUpperCase().padStart(4, '0');
  console.log(`\tdmaCmd\t${lenHex}, ${vramHex}\t\t\t; entry ${i}`);
}
